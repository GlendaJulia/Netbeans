
package com.tecsup.prueba;

public class Calculo {
    public int mayor(int a, int b){
        if (a > b ) {
             return a;
         }else{
             return b;
         }
    }
    public int menor(int a, int b){
        if (a < b ) {
             return a;
         }else{
             return b;
         }
    }
}
